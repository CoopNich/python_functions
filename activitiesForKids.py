def run(*kids):
    for kid in kids:
        print(f'{kid} ran like a fool, yo!')
def swing(*kids):
    for kid in kids:
        print(f'{kid} gained some air on the swingset, yo!')
def slide(*kids):
    for kid in kids:
        print(f'{kid} slid down the slide all like "weeeeee!!!".')
def hide_and_seek(*kids):
    for kid in kids:
        print(f'{kid} went stealth mode, yo!')


run("Joe", "Jenna", "Pam", "Sam", "Andrea")
print()
swing("Marybeth", "Ariel", "Kevin", "Courtney")
print()
slide("Mike", "Jack", "Jennifer", "Earl")
print()
hide_and_seek("Henry", "Heather", "Hayley", "Hugh")